const crypto = require('crypto');
const { Pool } = require('pg');

if (!process.env.DATABASE_URL) {
  throw new Error('Thieu DATABASE_URL trong file .env de ket noi Supabase/Postgres');
}

const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
  ssl: process.env.DB_SSL === 'false' ? false : { rejectUnauthorized: false },
  connectionTimeoutMillis: 10000
});

// DB cloud (Supabase) tu dong dong cac ket noi idle -> pg phat su kien 'error'
// tren client trong pool. Khong co listener thi Node coi la loi chua bat va lam
// CRASH ca server. Bat o day de pool tu tao lai ket noi, server khong sap.
pool.on('error', (err) => {
  console.error('Canh bao: ket noi DB idle bi dong (da bo qua, pool se tu tao lai):', err.message);
});

async function query(text, params = []) {
  const result = await pool.query(text, params);
  return result.rows;
}

async function get(text, params = []) {
  const rows = await query(text, params);
  return rows[0];
}

async function run(text, params = []) {
  return pool.query(text, params);
}

async function withTransaction(fn) {
  const client = await pool.connect();
  try {
    await client.query('BEGIN');
    const result = await fn(client);
    await client.query('COMMIT');
    return result;
  } catch (error) {
    await client.query('ROLLBACK');
    throw error;
  } finally {
    client.release();
  }
}

function hashPassword(password, salt) {
  return crypto.scryptSync(password, salt, 64).toString('hex');
}

function verifyPassword(password, salt, hash) {
  const candidate = Buffer.from(hashPassword(password, salt, 64), 'hex');
  const stored = Buffer.from(hash, 'hex');
  return candidate.length === stored.length && crypto.timingSafeEqual(candidate, stored);
}

async function initDb() {
  await run(`
    CREATE TABLE IF NOT EXISTS admins (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      username TEXT UNIQUE NOT NULL,
      password_hash TEXT NOT NULL,
      salt TEXT NOT NULL,
      ho_ten TEXT,
      role TEXT DEFAULT 'owner' CHECK (role IN ('owner', 'staff')),
      created_at TIMESTAMPTZ DEFAULT now()
    );

    CREATE TABLE IF NOT EXISTS customers (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      ten TEXT NOT NULL,
      dien_thoai TEXT UNIQUE NOT NULL,
      email TEXT,
      ghi_chu TEXT,
      created_at TIMESTAMPTZ DEFAULT now()
    );

    CREATE TABLE IF NOT EXISTS bookings (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      customer_id BIGINT REFERENCES customers(id) ON DELETE SET NULL,
      ho_ten TEXT NOT NULL,
      dien_thoai TEXT NOT NULL,
      email TEXT,
      diem_lay_hang TEXT NOT NULL,
      diem_giao_hang TEXT NOT NULL,
      loai_hang TEXT,
      trong_luong DOUBLE PRECISION,
      ghi_chu TEXT,
      gia_tri DOUBLE PRECISION,
      trang_thai TEXT DEFAULT 'moi' CHECK (trang_thai IN ('moi', 'da_bao_gia', 'dang_chay', 'hoan_thanh', 'huy')),
      created_at TIMESTAMPTZ DEFAULT now()
    );

    CREATE TABLE IF NOT EXISTS vehicles (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      ten TEXT NOT NULL,
      bien_so TEXT UNIQUE NOT NULL,
      loai TEXT,
      tai_trong TEXT,
      trang_thai TEXT DEFAULT 'san_sang' CHECK (trang_thai IN ('san_sang', 'dang_chay', 'bao_duong')),
      created_at TIMESTAMPTZ DEFAULT now()
    );

    CREATE TABLE IF NOT EXISTS trips (
      id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
      booking_id BIGINT REFERENCES bookings(id) ON DELETE SET NULL,
      vehicle_id BIGINT REFERENCES vehicles(id) ON DELETE SET NULL,
      tai_xe TEXT,
      ngay_khoi_hanh TEXT,
      trang_thai TEXT DEFAULT 'cho_chay' CHECK (trang_thai IN ('cho_chay', 'dang_chay', 'hoan_thanh', 'huy')),
      ghi_chu TEXT,
      created_at TIMESTAMPTZ DEFAULT now()
    );

    CREATE INDEX IF NOT EXISTS idx_bookings_customer_id ON bookings(customer_id);
    CREATE INDEX IF NOT EXISTS idx_bookings_created_at ON bookings(created_at);
    CREATE INDEX IF NOT EXISTS idx_trips_booking_id ON trips(booking_id);
    CREATE INDEX IF NOT EXISTS idx_trips_vehicle_id ON trips(vehicle_id);

    -- Bang luu phien dang nhap (connect-pg-simple)
    CREATE TABLE IF NOT EXISTS "session" (
      "sid" VARCHAR NOT NULL PRIMARY KEY,
      "sess" JSON NOT NULL,
      "expire" TIMESTAMP(6) NOT NULL
    );
    CREATE INDEX IF NOT EXISTS "IDX_session_expire" ON "session" ("expire");
  `);

  const adminCount = await get('SELECT COUNT(*)::int AS c FROM admins');
  if (adminCount.c === 0) {
    const salt = crypto.randomBytes(16).toString('hex');
    await run(
      "INSERT INTO admins (username, password_hash, salt, ho_ten, role) VALUES ($1, $2, $3, $4, 'owner')",
      ['admin', hashPassword('xuantruong123', salt), salt, 'Quan tri vien']
    );
    console.log('>> Da tao tai khoan admin mac dinh: admin / xuantruong123 - hay doi mat khau sau khi dang nhap!');
  }

  const vehicleCount = await get('SELECT COUNT(*)::int AS c FROM vehicles');
  if (vehicleCount.c === 0) {
    const vehicles = [
      ['Xe tai nho 1', 'CHUA-CAP-NHAT-01', 'thung_kin', '500kg-1T'],
      ['Xe tai nho 2', 'CHUA-CAP-NHAT-02', 'thung_kin', '500kg-1T'],
      ...Array.from({ length: 6 }, (_, i) => [`Xe 2.5 tan ${i + 1}`, `CHUA-CAP-NHAT-1${i + 1}`, 'mui_bat', '2-2.5T']),
      ...Array.from({ length: 3 }, (_, i) => [`Xe 5 tan ${i + 1}`, `CHUA-CAP-NHAT-2${i + 1}`, 'mui_bat', '5T']),
      ...Array.from({ length: 3 }, (_, i) => [`Xe 10 tan ${i + 1}`, `CHUA-CAP-NHAT-3${i + 1}`, 'mui_bat', '8-10T']),
      ['Xe dong lanh 1', 'CHUA-CAP-NHAT-41', 'dong_lanh', '1.5-10T']
    ];

    await withTransaction(async (client) => {
      for (const vehicle of vehicles) {
        await client.query(
          'INSERT INTO vehicles (ten, bien_so, loai, tai_trong) VALUES ($1, $2, $3, $4)',
          vehicle
        );
      }
    });
    console.log('>> Da nhap san 15 xe - vao trang admin de cap nhat bien so that.');
  }
}

module.exports = { pool, query, get, run, withTransaction, initDb, hashPassword, verifyPassword };
